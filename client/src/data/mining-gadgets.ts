export interface MiningModule {
  name: string;
  type: "active" | "passive";
  price: number;
  duration: number; // seconds, 0 = permanent/passive
  uses: number; // consumable uses, 0 = unlimited/passive
  miningLaserPower: number; // percentage modifier (e.g. 135 = 135% of base)
  laserInstability: number; // percentage modifier (e.g. -20 = -20%)
  resistance: number; // percentage modifier
  optimalChargeWindow: number; // percentage modifier
  optimalChargeRate: number; // percentage modifier
  overchargeRate: number; // percentage modifier
  shatterDamage: number; // percentage modifier
  extractionLaserPower: number; // percentage modifier
  inertMaterials: number; // percentage modifier
  clusterModifier: number; // percentage modifier
  description: string;
}

